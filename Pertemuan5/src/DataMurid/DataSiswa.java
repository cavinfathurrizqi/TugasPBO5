  private void tampilData() {
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("nama");
        model.addColumn("absen");
        model.addColumn("wali_kelas");
        model.addColumn("wali_murid");
       

// load data dari DB
        try (
               Connection conn = DriverManager.getConnection(DB_URL, USER, PASS);
                Statement st = conn.createStatement(); 
                ResultSet rs = st.executeQuery("SELECT * FROM datasiswa ORDER BY absen")) {

            while (rs.next()) {
                model.addRow(new Object[]{
                    rs.getString("nama"),
                    rs.getInt("absen"),
                    rs.getString("wali_kelas"),
                    rs.getString("wali_murid"),
                   
                });
            }

            Tabel.setModel(model); // tampilkan ke JTable
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal load data: " + e.getMessage());
        }
  }
