    private void jButtonInsertActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonInsertActionPerformed
      try {
            String nama = tfnama.getText();
            int absen = Integer.parseInt(tfabsen.getText());
            String wali_kelas = tfwalkel.getText();
            String wali_murid = tfwalmur.getText();

            connect();
            String sql = "INSERT INTO datasiswa (nama, absen, wali_kelas, wali_murid) VALUES (?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(sql);
            ps.setString(1, nama);
            ps.setInt(2, absen);
            ps.setString(3, wali_kelas);
             ps.setString(4, wali_murid);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!");
             bersih();   // Kosongkan textfield
            tampilData();
        } catch (NumberFormatException | SQLException e) {
            System.out.println("Gagal insert: " + e.getMessage());
        }
        
    }//GEN-LAST:event_jButtonInsertActionPerformed

    private void jButtonUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUpdateActionPerformed
        try {
            String sql = "UPDATE datasiswa SET nama = ?, absen = ?, wali_kelas = ?, wali_murid = ? WHERE absen = ? ";
           connect();
            PreparedStatement pst = conn.prepareStatement(sql);

            // Ambil dari textfield
            pst.setString(1, tfnama.getText());
            pst.setInt(2, Integer.parseInt(tfabsen.getText()));
            pst.setString(3, tfwalkel.getText());
            pst.setString(4, tfwalmur.getText());
            pst.setInt(5, Integer.parseInt(tfabsen.getText()));
            

            pst.executeUpdate();
            JOptionPane.showMessageDialog(this, "Data berhasil diupdate");

            // Refresh tabel
            tampilData();

            // Kosongkan textfield
            tfnama.setText("");
            tfabsen.setText("");
            tfwalkel.setText("");
            tfwalmur.setText("");
            

        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }
    }//GEN-LAST:event_jButtonUpdateActionPerformed

